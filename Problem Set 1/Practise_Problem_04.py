'''
In this program, I will create a program that will calculate the sum of two numbers.
These numbers will be entered by the user.
'''
num1 = int(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))
average = (num1 + num2) / 2
print(f"The average of {num1} and {num2} is {average}")